    document.addEventListener("DOMContentLoaded", () => {
    
        const orderPanel = document.getElementById("orderPanel");
        const orderItemsContainer = orderPanel.querySelector(".flex-1.overflow-y-auto");
        const orderTotal = document.getElementById("orderTotal"); // فرض شده این span در پنل هست
    
        let order = {};
    
        // --- Render کارت‌ها ---
        foods.forEach(food => {
            const section = document.querySelector(`#${food.category} .food-grid`);
            if(!section) return;
    
            const card = document.createElement("div");
            card.className = "bg-white rounded-xl shadow hover:shadow-lg transition p-4";
            card.innerHTML = `
                <img src="${food.img}" class="rounded-lg mb-4 w-full h-40 object-cover">
                <h3 class="font-bold text-lg">${food.name}</h3>
                <p class="text-gray-500">$${food.price}</p>
                <button class="orderBtn mt-3 bg-orange-600 text-white px-4 py-2 rounded-lg hover:bg-orange-700"
                    data-name="${food.name}"
                    data-price="${food.price}">
                    Order
                </button>
            `;
            section.appendChild(card);
    
            // Event Listener برای دکمه Order
            const btn = card.querySelector(".orderBtn");
            btn.addEventListener("click", () => {
                addToOrder(food.name, food.price);
            });
        });
    
        // --- توابع پنل ---
        function addToOrder(name, price) {
            if(order[name]) {
                order[name].qty += 1;
            } else {
                order[name] = { price: Number(price), qty: 1 };
            }
            renderOrder();
        }
    
        function renderOrder() {
            orderItemsContainer.innerHTML = "";
            let total = 0;
    
            Object.keys(order).forEach(name => {
                const item = order[name];
                total += item.price * item.qty;
    
                const div = document.createElement("div");
                div.className = "order-item flex items-center justify-between bg-gray-50 rounded-lg p-3 shadow-sm";
                div.innerHTML = `
                    <div class="flex-1">
                        <h4 class="font-semibold text-gray-700">${name}</h4>
                        <p class="text-sm text-gray-500">Delicious & fresh</p>
                    </div>
                    <div class="flex flex-col items-end gap-1">
                        <span class="font-bold text-orange-700">$${item.price}</span>
                        <div class="flex gap-2 items-center">
                            <button class="decrease bg-orange-700 text-white px-2 rounded hover:bg-orange-600">-</button>
                            <span class="px-1">${item.qty}</span>
                            <button class="increase bg-orange-700 text-white px-2 rounded hover:bg-orange-600">+</button>
                            <button class="remove text-red-600 px-2 font-bold">×</button>
                        </div>
                    </div>
                `;
    
                // دکمه‌ها
                div.querySelector(".increase").addEventListener("click", () => {
                    order[name].qty += 1;
                    renderOrder();
                });
                div.querySelector(".decrease").addEventListener("click", () => {
                    if(order[name].qty > 1) {
                        order[name].qty -= 1;
                    } else {
                        delete order[name];
                    }
                    renderOrder();
                });
                div.querySelector(".remove").addEventListener("click", () => {
                    delete order[name];
                    renderOrder();
                });
    
                orderItemsContainer.appendChild(div);
            });
    
            orderTotal.textContent = `$${total}`;
        }
    
        // --- پنل و بک‌دراپ ---
        const closeOrder = document.getElementById("closeOrder");
        const backdrop = document.getElementById("backdrop");
    
        closeOrder.addEventListener("click", () => {
            orderPanel.classList.add("translate-x-full");
        });
        backdrop.addEventListener("click", () => {
            orderPanel.classList.add("translate-x-full");
            backdrop.classList.add("hidden");
        });
    
    });
